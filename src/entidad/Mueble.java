package entidad;

public class Mueble {
	
	private int idMueble;
	private int precio;
	private int stock;
	
	public int getIdMueble() {
		return idMueble;
	}
	public void setIdMueble(int idMueble) {
		this.idMueble = idMueble;
	}
	public int getPrecio() {
		return precio;
	}
	public void setPrecio(int precio) {
		this.precio = precio;
	}
	public int getStock() {
		return stock;
	}
	public void setStock(int stock) {
		this.stock = stock;
	}
	
}
